/**
 * 指定日が国民の祝日・振替休日・国民の休日かどうか判定する
 * @param {number} year
 * @param {number} month - 0-indexed
 * @param {number} day
 * @returns {boolean}
 */
export function isHoliday(year, month, day) {
  const mm = String(month + 1).padStart(2, '0')
  const dd = String(day).padStart(2, '0')
  return HOLIDAY_DATES.has(`${year}-${mm}-${dd}`)
}

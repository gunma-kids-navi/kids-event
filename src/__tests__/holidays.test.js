/**
 * holidays.js のユニットテスト
 * isHoliday() の正確性・境界値・年範囲を検証する
 */

import { describe, it, expect } from "vitest";
import { isHoliday } from "../data/holidays.js";

// ── ヘルパー: "YYYY-MM-DD" 文字列から [year, month0, day] を返す
function ymd(str) {
  const [y, m, d] = str.split("-").map(Number);
  return [y, m - 1, d]; // month は 0-indexed
}

// ── 固定祝日の網羅テスト用データ (2026年)
const HOLIDAYS_2026 = [
  "2026-01-01", // 元日
  "2026-01-12", // 成人の日
  "2026-02-11", // 建国記念の日
  "2026-02-23", // 天皇誕生日
  "2026-03-20", // 春分の日
  "2026-04-29", // 昭和の日
  "2026-05-03", // 憲法記念日
  "2026-05-04", // みどりの日
  "2026-05-05", // こどもの日
  "2026-05-06", // 振替休日（憲法記念日 5/3 の代替）
  "2026-07-20", // 海の日
  "2026-08-11", // 山の日
  "2026-09-21", // 敬老の日
  "2026-09-22", // 国民の休日
  "2026-09-23", // 秋分の日
  "2026-10-12", // スポーツの日
  "2026-11-03", // 文化の日
  "2026-11-23", // 勤労感謝の日
];

// 2026年の「祝日でない」日サンプル
const NON_HOLIDAYS_2026 = [
  "2026-01-02", // 元日翌日（平日）
  "2026-05-01", // 5月1日（平日）
  "2026-05-07", // GW明け（平日）
  "2026-09-20", // 敬老の日の前日（日曜）
  "2026-12-25", // クリスマス（祝日ではない）
  "2026-12-31", // 大晦日
];

// ─────────────────────────────────────────────
describe("isHoliday - 2026年 祝日判定", () => {
  it.each(HOLIDAYS_2026)("%s は祝日と判定される", (dateStr) => {
    const [y, m, d] = ymd(dateStr);
    expect(isHoliday(y, m, d)).toBe(true);
  });

  it.each(NON_HOLIDAYS_2026)("%s は祝日ではない", (dateStr) => {
    const [y, m, d] = ymd(dateStr);
    expect(isHoliday(y, m, d)).toBe(false);
  });
});

// ─────────────────────────────────────────────

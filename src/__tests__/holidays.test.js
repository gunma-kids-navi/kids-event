// ─────────────────────────────────────────────
describe("isHoliday - 国民の休日・振替休日", () => {
  it("2026-09-22 は国民の休日（敬老の日と秋分の日に挟まれた火曜）", () => {
    expect(isHoliday(2026, 8, 22)).toBe(true); // month=8 → 9月
  });

  it("2026-05-06 は振替休日（憲法記念日 5/3 が日曜のため）", () => {
    expect(isHoliday(2026, 4, 6)).toBe(true); // month=4 → 5月
  });

  it("2025-02-24 は振替休日（天皇誕生日 2/23 が日曜）", () => {
    expect(isHoliday(2025, 1, 24)).toBe(true); // month=1 → 2月
  });

  it("2025-02-23 は天皇誕生日（日曜）でも祝日登録されている", () => {
    expect(isHoliday(2025, 1, 23)).toBe(true);
  });
});

// ─────────────────────────────────────────────
// BUG #29: isHoliday の年範囲が 2025〜2027 のみ
// → 2028年以降は祝日が表示されない（将来データへの対応なし）
describe("[BUG #29] isHoliday - 年範囲外は常に false を返す", () => {
  it("2024-01-01（元日）は範囲外で false", () => {
    // 2024年は範囲外 → 実際は元日だが false を返す
    expect(isHoliday(2024, 0, 1)).toBe(false);
  });

  it("2028-01-01（元日）は範囲外で false", () => {
    // 将来の祝日も false → カレンダーで 2028年以降は祝日表示なし
    expect(isHoliday(2028, 0, 1)).toBe(false);
  });
});

// ─────────────────────────────────────────────
// BUG #33: isHoliday に無効値を渡しても例外が出ない（バリデーションなし）
describe("[BUG #33] isHoliday - 無効な引数でもクラッシュしない", () => {
  it("month=-1（負の月）は false を返しクラッシュしない", () => {
    expect(() => isHoliday(2026, -1, 1)).not.toThrow();
    expect(isHoliday(2026, -1, 1)).toBe(false);
  });

  it("month=12（13月）は false を返しクラッシュしない", () => {
    expect(() => isHoliday(2026, 12, 1)).not.toThrow();
    expect(isHoliday(2026, 12, 1)).toBe(false);
  });

  it("day=0 は false を返しクラッシュしない", () => {
    expect(() => isHoliday(2026, 4, 0)).not.toThrow();
  });
});
